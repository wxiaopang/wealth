- (UIImage *)addWatermarkForText:(NSString *)mark {
    CGFloat w = self.size.width;
    CGFloat h = self.size.height;

    UIGraphicsBeginImageContext(self.size);
    [self drawInRect:CGRectMake(0, 0, w, h)];

    NSDictionary *dic = @{ NSFontAttributeName:[UIFont systemFontOfSize:50.0f],
                           NSForegroundColorAttributeName:[UIColor colorWithRed:220.0f/255.0f green:220.0f/255.0f blue:220.0f/255.0f alpha:1.0f] };
    CGSize size = [mark boundingRectWithSize:CGSizeMake(w, h)
                                     options:NSStringDrawingTruncatesLastVisibleLine
                                  attributes:dic
                                     context:nil].size;

    [mark drawInRect:CGRectMake((w - size.width)/2, 16, size.width, size.height) withAttributes:dic];
    UIImage *aimg = UIGraphicsGetImageFromCurrentImageContext();
    UIGraphicsEndImageContext();
    return aimg;
}

